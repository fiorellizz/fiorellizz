function gerarProdutos() {
    const container = document.getElementById('product-sections');
    
    Object.keys(produtos).forEach(categoria => {

        const categoriaTitulo = document.createElement('h3');
        categoriaTitulo.classList.add('section-subtitle-products');
        categoriaTitulo.innerText = categoria;
        container.appendChild(categoriaTitulo);
        
        const categoriaContainer = document.createElement('div');
        categoriaContainer.id = "dishes";
        
        produtos[categoria].forEach(produto => {
            const produtoDiv = document.createElement('div');
            produtoDiv.classList.add('dish');
            
            produtoDiv.innerHTML = `
                <div class="dish-heart">
                    <i class="fa-solid fa-heart"></i>
                </div>
                <img src="${produto.imagem}" class="dish-image" alt="${produto.nome}">
                <h3 class="dish-title">${produto.nome}</h3>
                <span class="dish-description">${produto.descricao}</span>
                <div class="dish-quantity">Quantidade disponível: <span>${produto.quantidade}</span></div>
                <div class="dish-rate">
                    ${'<i class="fa-solid fa-star"></i>'.repeat(5)}
                    <span>(${produto.avaliacao}+)</span>
                </div>
                <div class="dish-price">
                    <h4>${produto.preco}</h4>
                    <button class="btn-default">
                        <i class="fa-solid fa-basket-shopping"></i>
                    </button>
                </div>
            `;
            
            categoriaContainer.appendChild(produtoDiv);
        });
        
        container.appendChild(categoriaContainer);
    });
}

gerarProdutos();